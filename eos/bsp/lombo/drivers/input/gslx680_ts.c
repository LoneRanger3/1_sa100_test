/**
 * gslx680_i2c_write - write data to gslx680 tp register by i2c
 * @reg: register address of gslx680 to write
 * @buf: the data to write
 * @len: the length of data
 *
 * return RT_EOK if success, RT_ERROR if failed
 */
static rt_err_t gslx680_i2c_write(u8 reg, u8 *buf, u32 len)
{
	struct rt_i2c_msg m;
	u8 msg_buf[len + 1];

	msg_buf[0] = reg;
	if (len > 0)
		rt_memcpy(msg_buf + 1, buf, len);

	m.addr  = GSLX680_I2C_WADDR;
	m.flags = RT_I2C_WR;
	m.buf   = msg_buf;
	m.len   = len + 1;

	if (rt_i2c_transfer(i2c_gslx680_dev, &m, 1) != 1) {
		LOG_E("rt_i2c_transfer error");
		return -RT_ERROR;
	}

	return RT_EOK;
}

/**
 * gslx680_i2c_read - read data from gslx680 tp register by i2c
 * @reg: register address of gslx680 to read
 * @buf: the data to store
 * @len: the length of data
 *
 * return RT_EOK if success, RT_ERROR if failed
 */
static rt_err_t gslx680_i2c_read(u8 reg, u8 *buf, u32 len)
{
	struct rt_i2c_msg msgs[2];

	if (i2c_gslx680_dev == RT_NULL) {
		LOG_E("Can't find focaltech touch panel I2C device");
		return -RT_ERROR;
	}

	msgs[0].addr  = GSLX680_I2C_RADDR;
	msgs[0].flags = RT_I2C_WR;
	msgs[0].buf   = &reg;
	msgs[0].len   = 1;

	msgs[1].addr  = GSLX680_I2C_RADDR;
	msgs[1].flags = RT_I2C_RD;
	msgs[1].buf   = buf;
	msgs[1].len   = len;

	if (rt_i2c_transfer(i2c_gslx680_dev, msgs, 2) != 2) {
		LOG_E("rt_i2c_transfer error");
		return -RT_ERROR;
	}
	/* for (i =0; i < len; i++) {
		LOG_E("reg: %d buf[%d] 0x%x\n", reg, i, buf[i]);
	} */
	return RT_EOK;
}

/* set gpio pin direction output and level */
static void gslx680_set_gpio_output(enum gpio_port port, enum gpio_pin pin, int value)
{
	struct pinctrl *pctrl;
	int pin_num;
	rt_err_t ret;

	RT_ASSERT((value == 0) || (value == 1));

	pctrl = pinctrl_get(GSLX680_TS_MODULE_NAME);
	if (pctrl == RT_NULL) {
		LOG_E("pinctrl_get return RT_NULL");
		return;
	}

	pin_num = pinctrl_gpio_request(pctrl, port, pin);
	if (pin_num < 0) {
		LOG_E("pin_num < 0");
		return;
	}

	ret = pinctrl_gpio_direction_output(pctrl, pin_num);
	if (ret != RT_EOK) {
		LOG_E("pinctrl_gpio_direction_output != RT_EOK");
		return;
	}

	ret = pinctrl_gpio_set_value(pctrl, pin_num, value);
	if (ret != RT_EOK) {
		LOG_E("pinctrl_gpio_set_value != RT_EOK");
		return;
	}
}

