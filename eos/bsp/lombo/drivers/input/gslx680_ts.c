/* touch panel interrupt handle function */
static void gslx680_ts_irq_handler(void *data)
{
	/* send sleep command will create an interrupt, ignore it */
	if (sleep_cmd) {
		LOG_E("sleep command, ignore this interrupt");
		sleep_cmd = RT_FALSE;
		return;
	}
	LOG_D("gslx680_ts_irq_handler\n");
	gslx680_ts_free_gpio_irq();
	input_send_int_event(INPUT_EVENT_TOUCH);
}

/* request gpio interrupt for tp INT pin */
static void gslx680_ts_request_gpio_irq()
{
	struct pinctrl *pctrl;
	struct gpio_irq_data irq_data;
	int pin_num, ret;

	pctrl = pinctrl_get(GSLX680_TS_MODULE_NAME);
	if (pctrl == RT_NULL) {
		LOG_E("pctrl == RT_NULL");
		return;
	}

	pin_num = pinctrl_gpio_request(pctrl, GPIO_PORT_E, GPIO_PIN_10);
	if (pin_num < 0) {
		LOG_E("pin_num < 0");
		return;
	}

	irq_data.handler = gslx680_ts_irq_handler;	/* interrupt handle function */
	irq_data.irq_arg = NULL;
	irq_data.clock_src = GPIO_IRQ_HOSC_24MHZ;
	irq_data.clock_src_div = 10;
	irq_data.trig_type = EINT_TRIG_RISING_EDGE;

	ret = pinctrl_gpio_request_irq(pctrl, pin_num, &irq_data);
	if (ret != RT_EOK) {
		LOG_E("pinctrl_gpio_request_irq != RT_EOK");
		return;
	}
}

/* get gslx680 touch panel product id and version */
static rt_err_t gslx680_read_id(void)
{
	rt_err_t ret;
	u8 buf[4] = {0};
	LOG_E("%s %d\n", __func__, __LINE__);
	/* register address from 0x8140 to 0x8145, product id and version */
	ret = gslx680_i2c_read(GSLX680_REG_VERSION, buf, 4);
	if (ret != RT_EOK) {
		LOG_E("get GSLX680_REG_VERSION error");
		return ret;
	}

	/* Product ID */
	rt_memset(tpd_info.pid, 0, 4);
	rt_memcpy(tpd_info.pid, buf, 4);

	/* Firmware version */
	tpd_info.vid = buf[2];
	LOG_E("firmware version: %x", tpd_info.vid);

	if (tpd_info.vid == 0)
		return RT_ERROR;

	return RT_EOK;
}

/* handle touch panel event and return number of touch */
static u8 gslx680_event_handler(void)
{
	u8 point_data[GSLX680_PER_COORD_LEN * GSLX680_MAX_TOUCH_POINT + 4] = {0};
	u8 touch_num = 0;
	int i;

	 memset(point_data, 0, sizeof(point_data));
	if (gslx680_i2c_read(GSLX680_DATA_REG, point_data, sizeof(point_data)) ==
		RT_EOK) {
		touch_num = point_data[0];
		for (i = 0; i < (touch_num < GSLX680_MAX_TOUCH_POINT ? touch_num :
			GSLX680_MAX_TOUCH_POINT); i++) {
			point[i].x = ((point_data[4 * i + 5] & 0xf) << 8) |
				point_data[4 * i + 4];
			point[i].y = ((point_data[4 * i + 7] & 0xf) << 8) |
				point_data[4 * i + 6];
			point[i].id = (point_data[4 * i + 7] & 0xf0) >> 4;
		}
	}
	return touch_num;
}

/* read touch points data and report event */
static void gslx680_read_point()
{
	int i, tmp;
	struct sw_touch_panel_platform_data *tpd;

	tpd = _touch_panel_data;
	if (tpd == RT_NULL) {
		LOG_W("gslx680_read_point: _touch_panel_data == RT_NULL");
		return;
	}
	tpd->points.point_num = gslx680_event_handler();
	for (i = 0; i < tpd->_MAX_POINT; i++) {
		tpd->points.p[i].e = 0xff;
		if (tpd->points.point_num == 0) {
			if (tpd->points.p[i].c < GSLX680_CNT_DOWN)
				tpd->points.p[i].e = 0xff;
			else
				tpd->points.p[i].e = TOUCH_EVENT_UP;
			tpd->points.p[i].c = 0;
		} else {
			tpd->points.p[i].c++;

			if (tpd->points.p[i].c == GSLX680_CNT_DOWN) {
				tpd->points.p[i].x = point[i].x;
				tpd->points.p[i].y = point[i].y;
				tpd->points.p[i].e = TOUCH_EVENT_DOWN;
			} else if (tpd->points.p[i].c > GSLX680_CNT_DOWN) {
				if (point[i].x != tpd->points.p[i].x ||
					point[i].y != tpd->points.p[i].y) {
					tmp = GSLX680_CNT_DOWN + GSLX680_CNT_DOWN;
					if (tpd->points.p[i].c > tmp) {
						tpd->points.p[i].x = point[i].x;
						tpd->points.p[i].y = point[i].y;
						tpd->points.p[i].e = TOUCH_EVENT_MOVE;
						tpd->points.p[i].c = GSLX680_CNT_DOWN;
					}
				}

				tmp = GSLX680_CNT_LONG + GSLX680_CNT_HOLD;
				if (tpd->points.p[i].c == tmp) {
					tpd->points.p[i].x = point[i].x;
					tpd->points.p[i].y = point[i].y;
					tpd->points.p[i].e = TOUCH_EVENT_HOLD;
				}

				tmp = GSLX680_CNT_LONG + GSLX680_CNT_HOLD;
				if (tpd->points.p[i].c > tmp)
					tpd->points.p[i].c = GSLX680_CNT_LONG;
			}
		}

		if (tpd->points.p[i].e != 0xff) {
			if (tpd->points.p[i].e == TOUCH_EVENT_HOLD)
				continue;

			/* report event */
			input_report_abs(&_ts_dev,
					ABS_MT_TRACKING_ID,
					i);
			input_report_abs(&_ts_dev,
				ABS_MT_TOUCH_MAJOR,
				tpd->points.p[i].e);
			input_report_abs(&_ts_dev,
				ABS_MT_POSITION_X,
				(tpd->points.p[i].x*tpd->_DISP_W/tpd->_MAX_X)-10);
			input_report_abs(&_ts_dev,
				ABS_MT_POSITION_Y,
				(tpd->points.p[i].y*tpd->_DISP_H/tpd->_MAX_Y)-10);
			input_mt_sync(&_ts_dev);
#if 1
			LOG_W("[id: %d](event: %d) (x: %d, y: %d)", i,
				tpd->points.p[i].e,
				tpd->points.p[i].x,
				tpd->points.p[i].y);
#endif
		}
	}
}

